function generowanie_glownego_pliku_KML(KolekcjaObiektow, ustawieniaKML, czyZapisac, parametryWszystkichKolekcji){
    let PLIK_KML = '';
    PLIK_KML += KML_zaczyna;
    PLIK_KML += '<name>Mapa II RP (l.30 XX w.)</name>'
    PLIK_KML += '<open>1</open>';
    PLIK_KML += KML_central;
    PLIK_KML += '<Style><ListStyle><listItemType>radioFolder</listItemType><bgColor>00ffffff</bgColor><maxSnippetLines>2</maxSnippetLines></ListStyle></Style>';
    let PLIK_KML_NetworkLink = "";
    parametryWszystkichKolekcji.forEach(parametryKolekcji => {
        const tekst = {
            tytul: parametryKolekcji["nazwa"],
            opis: parametryKolekcji["opis"],
            KML: KolekcjaObiektow["kml"].slice(-2).join("/")+'.kml'
        };        
        PLIK_KML_NetworkLink += KML_NetworkLink1(tekst, KolekcjaObiektow["gps"], ustawieniaKML);  
    });
    PLIK_KML += PLIK_KML_NetworkLink;
    PLIK_KML += KML_konczy;
    zapisz_KML(PLIK_KML, KolekcjaObiektow["kml"].slice(0,1).join("/"), czyZapisac);
}

function generowanie_pojedynczego_plik_KML(KolekcjaObiektow, ustawieniaWczytywaniaKML, czyZapisac, parametry){    
    let PLIK_KML = '';
    PLIK_KML += KML_zaczyna;
    PLIK_KML += '<name>zoom</name>'
    if(KolekcjaObiektow["kml"].length ===2){
        PLIK_KML += KML_central;
        PLIK_KML += '<ScreenOverlay><visibility>1</visibility><Icon><href>';
        PLIK_KML += parametry["wskaznik"];
        PLIK_KML += '</href></Icon><overlayXY x="0.5" y="0.5" xunits="fraction" yunits="fraction"/><screenXY x="0.5" y="0.5" xunits="fraction" yunits="fraction"/><rotationXY x="0.5" y="0.5" xunits="fraction" yunits="fraction"/><size x="0" y="0" xunits="pixels" yunits="pixels"/></ScreenOverlay>';
    }
    let PLIK_KML_GroundOverlay = "";
    let PLIK_KML_NetworkLink = "";
    for(let NazwaObiektu in KolekcjaObiektow){
        if(NazwaObiektu!="gps" && NazwaObiektu!="img" && NazwaObiektu!="kml" && NazwaObiektu!="dir"){
            const Obiekt = KolekcjaObiektow[NazwaObiektu];
            const tekst = {
                IDN: NazwaObiektu,
                IMG: Obiekt["img"],
                KML: Obiekt["kml"].slice(-2).join("/")+'.kml'
            };     
            PLIK_KML_GroundOverlay += KML_GroundOverlay2(tekst, KolekcjaObiektow[NazwaObiektu]["gps"], ustawieniaWczytywaniaKML);
            PLIK_KML_NetworkLink += KML_NetworkLink2(tekst, KolekcjaObiektow[NazwaObiektu]["gps"], ustawieniaWczytywaniaKML);      
        }
    }   
    PLIK_KML += PLIK_KML_NetworkLink;
    PLIK_KML += PLIK_KML_GroundOverlay;
    PLIK_KML += KML_konczy;
    //console.log(KolekcjaObiektow["kml"]);
   zapisz_KML(PLIK_KML, KolekcjaObiektow["kml"].join("/"), czyZapisac);
}


